package api;

public class ExplorandoMath {
    public static void main(String[] args) {
        //seno, cosseno, tag, log
        System.out.println(Math.PI);
        System.out.println(Math.pow(2,3));
        System.out.println(Math.abs(-10.5));
        System.out.println(Math.min(70,7));
        System.out.println(Math.max(71,7));
        System.out.println(Math.ceil(6.55)); //Arredonda para cima
        System.out.println(Math.floor(6.55)); //Arredonda para baixo
        System.out.println(Math.random());
        System.out.println(Math.atan(4.5));
    }
}
