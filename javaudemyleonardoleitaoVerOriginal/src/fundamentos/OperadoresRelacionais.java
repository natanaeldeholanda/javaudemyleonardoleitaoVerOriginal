package fundamentos;

public class OperadoresRelacionais {

	public static void main(String[] args) {
		// Operadores -> ==, !=, >=, >,  <= e <.
		int num1 = 97;
		int num2 = 'a';
		System.out.println(num1 == num2); //Comparação se num1 é igual a num2.
		System.out.println(3 != 2); //Diferença num1 é diferente a num2.
		System.out.println(3 >= 2); 
		System.out.println(3 > 2); 
		System.out.println(3 <= 3); 
		System.out.println(3 < 4); 

	}

}
